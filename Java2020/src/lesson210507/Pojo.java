package lesson210507;

public class Pojo {

	private int w;
	private String n;
	private boolean strong;

	public int getWeight() {
		return w;
	}

	public void setWeight(int weight) {
		this.w = weight;
	}

	public String getName() {
		return n;
	}

	public void setName(String name) {
		this.n = name;
	}

	boolean isStrong() {
		return strong;
	}

	void setStrong(boolean strong) {
		this.strong = strong;
	}

}
